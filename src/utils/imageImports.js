// Helper function to get image by category and name
export const getImage = (category, name) => {
  const categories = {
    hero: heroImages,
    background: backgroundImages,
    service: serviceImages,
    team: teamImages,
    product: productImages,
    testimonial: testimonialImages,
    logo: logoImages,
    ui: uiImages,
    misc: miscImages,
  };

  const categoryObj = categories[category];
  if (!categoryObj) {
    console.warn(`Category "${category}" not found`);
    return null;
  }

  return categoryObj[name] || null;
};

